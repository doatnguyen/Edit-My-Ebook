    public class ClearTextPrompt extends FocusAdapter {

        private String prompt;

        public ClearTextPrompt(String prompt) {
            this.prompt = prompt;
        }

        @Override
        public void focusLost(FocusEvent e) {
            String s;
            try {
                switch (prompt) {
                    case "title": {
                        JTextField t = (JTextField) e.getSource();
                        s = t.getText();
                        if (!s.equals(title)) {
                            title = s;
                            list_chap.add(new String[]{s, content});
                            pos++;
                            btn_undo.setEnabled(true);
                            System.out.println("has change title");
                        }
                        break;
                    }
                    case "content": {
                        JEditorPane t = (JEditorPane) e.getSource();
                        s = t.getText();
                        if (!s.equals(content)) {
                            content = s;
                            list_chap.add(new String[]{title, s});
                            pos++;
                            btn_undo.setEnabled(true);
                            System.out.println("has change content");

                        }
                        break;
                    }
                }

            } catch (NullPointerException ex) {
                return;
            }
        }
    }

