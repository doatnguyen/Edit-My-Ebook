    private void btn_undoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_undoActionPerformed
        // TODO add your handling code here:
        if (pos > 0) {
            String[] c = list_chap.get(pos - 1);
            title = c[0];
            content = c[1];
            refectView();
            btn_redo.setEnabled(true);
            pos--;
            if (pos == 0) {
                btn_undo.setEnabled(false);
            }
        }

    }//GEN-LAST:event_btn_undoActionPerformed

    private void btn_redoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_redoActionPerformed
        // TODO add your handling code here:
        if (pos <= list_chap.size() - 2) {
            String[] c = list_chap.get(pos + 1);
            title = c[0];
            content = c[1];
            refectView();
            btn_undo.setEnabled(true);
            pos++;
            if (pos == (list_chap.size() - 1)) {
                btn_redo.setEnabled(false);
            }
        }
    }//GEN-LAST:event_btn_redoActionPerformed

    private void btn_repalce_allActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_repalce_allActionPerformed
        // TODO add your handling code here:
        String find = null, replace = "";
        try {
            find = txt_find_text.getText();
        } catch (NullPointerException e) {
            find = null;
        }
        try {
            replace = txt_replace_text.getText();
        } catch (NullPointerException e) {
            replace = "";
        }
        if (find != null) {
            content = content.replaceAll(find, replace);
            if (ch_find_title.isSelected()) {
                title = title.replaceAll(find, replace);
            }
            refectView();
        }
        System.out.println("null");
    }//GEN-LAST:event_btn_repalce_allActionPerformed

