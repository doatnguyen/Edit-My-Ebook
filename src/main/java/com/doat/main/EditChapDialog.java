    public void setData(TOCReference toc) {
        try {
            this.toc = toc;
            title = toc.getTitle();
            content = new String(toc.getResource().getData(), "UTF-8");
            txt_chap_name.setText(title);
            txt_chap_content.setContentType("text/html");
            txt_chap_content.setText(content);
            txt_chap_content.setCaretPosition(0);
            txt_chap_content.getDocument().addDocumentListener(new MyDocumentListener());
            txt_chap_content.addFocusListener(new ClearTextPrompt("content"));
            txt_chap_name.getDocument().addDocumentListener(new MyDocumentListener());
            txt_chap_name.addFocusListener(new ClearTextPrompt("title"));
            content = txt_chap_content.getText();
            String[] chap_source = new String[]{title, content};
            list_chap.add(chap_source);
        } catch (IOException ex) {
            showErrorMess("Lỗi Đọc Chương");
        }

    }

    private void refectView() {
        txt_chap_name.setText(title);
        txt_chap_content.setText(content);
        txt_chap_content.setCaretPosition(0);
    }

    private void saveData() {
        isChange = false;
        String c_title, c_content;
        try {
            c_title = txt_chap_name.getText();
        } catch (NullPointerException e) {
            c_title = "No Name";
        }
        try {
            c_content = txt_chap_content.getText();
        } catch (NullPointerException e) {
            c_content = " ";
        }
        toc.setTitle(c_title);
        toc.getResource().setData(c_content.getBytes(Charset.forName("utf-8")));
    }

